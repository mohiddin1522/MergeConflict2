
marks = int(input("Enter your marks: "))
if (marks <= 100 and marks >= 86):
    print("Grade A")
elif (marks <= 85 and marks >= 71):
    print("Grade B")
elif marks <= 70 and marks >= 61:
    print("Grade C")
elif marks <= 60 and marks >= 51:
    print("Grade D")
elif marks <= 50 and marks >= 36:
    print("Grade E")
elif marks <= 35:
    print("Grade F") 
    
    
